      Chaque amapien s'engage, par un contrat direct avec le producteur, à payer à l'avance la fourniture de la production nourricière et à en prendre livraison à chaque distribution.<br />
      En contrepartie, le producteur s'engage à cultiver dans le respect de la qualité sanitaire des produits et de l'environnement.
      Par ce système, les mangeurs garantissent au fermier un revenu et l'assurance de l'écoulement de sa production.<br /></p>
