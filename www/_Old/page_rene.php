<span style="color:#00FFFF; text-decoration:underline; font-size:16px; font-family:Arial, serif;">
Pourquoi vendre aux AMAP.</span><br />
Pour vendre ma production, jai choisi en priorité les AMAP, les rapports humains y sont riches déchanges et de sincérité, et des plus conviviaux.<br />
Je suis aussi sensible à cet engagement mutuel entre le mangeur et le producteur, pour moi cest un symbole très fort.<br />
La mutualisation des risques avec le mangeur, ainsi que la durée des contrats calqués sur le rythme biologique permet dassurer la qualité et aussi la sûreté des produits fournis. Je mengage à respecter les principes de la chartre établie par lassociation Alliance Provence qui anime le réseau des AMAP. 
<br /><br />
<span style="color:#00FFFF; text-decoration:underline; font-size:16px; font-family:Arial, serif;">
Qualité des ufs.</span><br />
La qualité de luf est due au mode délevage de la poule « plein air », ainsi quà son alimentation « bio »,  équilibre et diversité des céréales et des protéagineux qui composent la ration. Elle est aussi due à son mode de conservation, un uf pour rester vivant doit être conservé à température ambiante soit entre 8° et 16°, si vous le mettez au réfrigérateur à 3° ou 4°, luf meurt, il nen devient pas inconsommable ou malsain mais il est mort, et en étant totalement subjectif, il y perd certainement quelque chose.<br />
Pour moi la qualité principale dun uf est la fraîcheur, et pour être plus précis à la garantie de fraîcheur, je vous fournirai en vous indiquant la date de ponte, en conservant mes ufs à la température ad hoc et en vous livrant régulièrement.<br />
